/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
/**
 * @author initiald0824
 * @date 2020/3/1 9:52 下午
 */

public class RemoveNthFromEnd {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode dummy = new ListNode(0);
        dummy.next = head;
        ListNode p = dummy;
        ListNode front = head;
        ListNode behind = head;
        while (n-- > 0) {
            front = front.next;
        }
        while (front != null) {
            p = p.next;
            front = front.next;
            behind = behind.next;
        }
        p.next = behind.next;
        return dummy.next;
    }
}
